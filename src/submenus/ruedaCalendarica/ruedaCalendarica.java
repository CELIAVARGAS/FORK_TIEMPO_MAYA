    private void verificarFecha() {
        if (boxDate.getDate() != null) {
            //fecha pivote
            String Dateinicio = "2001-01-01";
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            try {
                Date fechaInicio = date.parse(Dateinicio);
                long fechaFinalMs = fechaInicio.getTime();

                long fechaInicialMs = boxDate.getDate().getTime();

                long diferencia = fechaFinalMs - fechaInicialMs;

                double dias = Math.floor(diferencia / (1000 * 60 * 60 * 24));
                System.out.println(dias);
                int diasTranscurridos = 0;

                //si da negativo es porque paso del anio 2001
                if (dias < 0) {
                    diasTranscurridos = (int) (dias * (-1));
                    sobrePasaFechaPivote = true;
                } else {
                    diasTranscurridos = (int) (dias);
                    sobrePasaFechaPivote = false;
                }
                System.out.println("---------------------------- ");
                calculoRuedaCalendarica cRC = new calculoRuedaCalendarica();
                cRC.calculoCuentaLarga(diasTranscurridos, sobrePasaFechaPivote, txtBaktun, txtKatun, 
                        txtTun, txtUinal, txtKin);
                cRC.calculoTzolqin(diasTranscurridos, sobrePasaFechaPivote,icTzol, lblNombreT, diaTzol);
                cRC.calculoHaab(diasTranscurridos, sobrePasaFechaPivote, icH, lblNombeH, diaHab);

            } catch (ParseException ex) {
                Logger.getLogger(ruedaCalendarica.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe de seleccionar o ingresar una fecha");
        }
    }

